# ─────────────────────────────────────────────

def main():
    print("=" * 62)
    print("    ADVANCED STOCK PREDICTION & ANALYSIS SYSTEM  v2.1")
    print("=" * 62)
    print("\nAvailable Tickers:")
    print(", ".join(TICKERS))

    # ── Ticker Validation ──────────────────────────────────────
    while True:
        selected_ticker = input("\nEnter Ticker from the list above: ").upper().strip()
        if selected_ticker in TICKERS:
            break
        print(f"  Invalid ticker. Please choose from: {', '.join(TICKERS)}")

    # ── Data Preparation ───────────────────────────────────────
    print(f"\n[1/5] Fetching 3 years of data for {selected_ticker}...")
    df_data = get_data(selected_ticker)
    print(f"    Total rows after indicator calculation: {len(df_data)}")
    print(f"    Features used: {', '.join(FEATURE_COLS)}")

    # Scale all features to [0, 1] — required for LSTM stability
