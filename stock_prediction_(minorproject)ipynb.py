    scaled_data = scaler.fit_transform(df_data)

    # Build supervised sequences
    X, y = build_sequences(scaled_data, TIME_STEPS)

    # Chronological split (no shuffling — preserves time order)
    split   = int(len(X) * TRAIN_RATIO)
    X_train, X_test = X[:split], X[split:]
    y_train, y_test = y[:split], y[split:]

    print(f"    Training samples : {len(X_train)}")
    print(f"    Testing  samples : {len(X_test)}")

    # ── Model Training ─────────────────────────────────────────
    print(f"\n[2/5] Training models for {selected_ticker}...")

    # XGBoost needs flat 2D input (samples × features)
    X_train_flat = X_train.reshape(X_train.shape[0], -1)
    X_test_flat  = X_test.reshape(X_test.shape[0],  -1)

    print("    Training XGBoost (gradient boosting)...")
    xgb_model  = build_xgboost(X_train_flat, y_train)

    print("    Training BiLSTM (with EarlyStopping)...")
    lstm_model = build_lstm(X_train, y_train)

    xgb_preds  = xgb_model.predict(X_test_flat)
    lstm_preds = lstm_model.predict(X_test, verbose=0).flatten()

    # ── Model Evaluation ───────────────────────────────────────
    print(f"\n[3/5] Evaluating models...")

    # Convert scaled predictions back to USD for meaningful metrics
    actual_prices    = [inverse_close(v, scaler, len(FEATURE_COLS)) for v in y_test]
    xgb_prices       = [inverse_close(v, scaler, len(FEATURE_COLS)) for v in xgb_preds]
    lstm_prices      = [inverse_close(v, scaler, len(FEATURE_COLS)) for v in lstm_preds]

    xgb_metrics  = compute_metrics(actual_prices, xgb_prices)
    lstm_metrics = compute_metrics(actual_prices, lstm_prices)

    print(f"\n  {'Metric':<8}  {'XGBoost':>10}  {'BiLSTM':>10}")
    print(f"  {'─'*8}  {'─'*10}  {'─'*10}")
    for metric in ['MAE', 'RMSE', 'MAPE']:
        xv = xgb_metrics[metric]
        lv = lstm_metrics[metric]
        unit = '%' if metric == 'MAPE' else '$'
        print(f"  {metric:<8}  {unit}{xv:>9.2f}  {unit}{lv:>9.2f}")

    # ── Model Selection (by RMSE — penalises large errors more) ──
    if xgb_metrics['RMSE'] <= lstm_metrics['RMSE']:
        best_name    = "XGBoost"
        best_model   = xgb_model
        best_type    = "flat"
        best_preds   = xgb_prices
        best_metrics = xgb_metrics
    else:
        best_name    = "BiLSTM"
        best_model   = lstm_model
        best_type    = "sequence"
        best_preds   = lstm_prices
        best_metrics = lstm_metrics

    print(f"\n  Best model selected: {best_name}")
    print(f"     MAE  : ${best_metrics['MAE']:.2f}")
    print(f"     RMSE : ${best_metrics['RMSE']:.2f}")
    print(f"     MAPE : {best_metrics['MAPE']:.2f}%")

    mae_val = best_metrics['MAE']

    # ── Save Models ────────────────────────────────────────────
    print(f"\n[4/5] Saving trained models to disk...")
    save_models(xgb_model, lstm_model, selected_ticker)

    # ── Performance Chart ──────────────────────────────────────
    print(f"\n[5/5] Displaying model performance chart...")

    errors = np.abs(np.array(actual_prices) - np.array(best_preds))

    plt.figure(figsize=(14, 6))
    plt.plot(actual_prices,  label='Actual Price',
             color='#2c3e50', alpha=0.85, linewidth=1.5)
    plt.plot(best_preds, label=f'Predicted ({best_name})',
             color='#e74c3c', linestyle='--', linewidth=1.5)
    plt.fill_between(
        range(len(best_preds)),
        np.array(best_preds) - errors,
        np.array(best_preds) + errors,
        alpha=0.1, color='#e74c3c', label='Prediction Error Band'
    )
